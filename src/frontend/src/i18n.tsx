interface I18nState {
  lang: Language;
  locale: string;
  setLang: (lang: Language) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nState | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Language>(() => {
    const stored = localStorage.getItem("sanatorium-lang");
    return stored === "en" ? "en" : "ru";
  });

  useEffect(() => {
    localStorage.setItem("sanatorium-lang", lang);
    document.documentElement.lang = lang;
    document.title = dictionaries[lang].app_title ?? dictionaries.ru.app_title;
  }, [lang]);

  const value = useMemo<I18nState>(() => {
    const t = (key: string, vars?: Record<string, string | number>) => {
      const template = dictionaries[lang][key] ?? dictionaries.ru[key] ?? key;
      if (!vars) return template;
      return Object.entries(vars).reduce(
        (acc, [varKey, varValue]) => acc.replaceAll(`{${varKey}}`, String(varValue)),
        template,
      );
    };
    const locale = lang === "en" ? "en-US" : "ru-RU";
    return { lang, locale, setLang, t };
  }, [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
